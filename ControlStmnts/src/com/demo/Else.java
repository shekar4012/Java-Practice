package com.demo;

public class Else {
	public static void main(String[] args) {
		int x=10;
		int y=5;
		int z=20;
		if (x<=y || y>=z) {   //"if" condition is not satisfied
		int a=x+y+z;
		System.out.println(a);
		}
	
		else { //"else" implements condition alternate to "if" condition
		System.out.println("my name is rajashekar");
	}
}
}
