package com.demo;

public class Switch2 {
	public static void main(String[] args) {
	
	String employee="nandana";
	
	switch (employee) {
	
	case "nandana":
		System.out.println("welcome nandana");
	break;
	
	case "raja":
		System.out.println("welcome raja");
	break;
	
	case "shravya":
		System.out.println("welcome shravya");
	break;
	
	default:
		System.out.println("no guests");
	}
	}
}
