package com.demo;

public class SwitchDemo {
public static void main(String[] args) {
	int employee=0;
	
	switch (employee) {
	case 1:
	System.out.println("General Managaer");	
	break;
	
	case 2:
		System.out.println("Accounts Manager");
	break;
	default:
		System.out.println("No employee selected");
}
}
}